# Demonstrating Escape Sequences in Python

print("1. New Line: Hello\nWorld")

print("2. Tab Space: Hello\tWorld")

print("3. Backslash: C:\\Users\\Sarah")

print("4. Single Quote: It\'s a beautiful day")

print("5. Double Quote: He said, \"Python is fun!\"")

print("6. Carriage Return: Hello\rWorld")  # "World" will overwrite "Hello" on most terminals

print("7. Backspace: Helloo\b!")  # Removes one 'o' before !

print("8. Form Feed: First Page\fSecond Page")  # Rarely visible, depends on printer/screen

print("9. Bell Sound: \a")  # May make a beep sound (not always audible on modern systems)
