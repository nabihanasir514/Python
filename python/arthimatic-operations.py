
# Define two numbers
a = 12
b = 4

# Addition
add_result = a + b
print("Addition:", add_result)

# Subtraction
sub_result = a - b
print("Subtraction:", sub_result)

# Multiplication
mul_result = a * b
print("Multiplication:", mul_result)

# Division (float)
div_result = a / b
print("Division (float):", div_result)

# Floor Division
floor_div = a // b
print("Floor Division:", floor_div)

# Modulus
mod_result = a % b
print("Modulus (remainder):", mod_result)

# Exponentiation
exp_result = a ** b
print("Exponentiation:", exp_result)
