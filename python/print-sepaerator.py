# Default separator (space)
print("Python", "is", "awesome")
# Output: Python is awesome

# Custom separator
print("Python", "is", "awesome", sep="-")
# Output: Python-is-awesome

print("2025", "07", "25", sep="/")
# Output: 2025/07/25

# No separator (join with nothing)
print("Hello", "World", sep="")
# Output: HelloWorld
